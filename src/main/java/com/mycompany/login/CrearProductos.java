    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        Producto p = new Producto();
        p.setNombreProd(nombreTextField.getText());
        p.setDescProd(descProdTextField.getText());
        p.setpUnitario(Double.parseDouble(pUnitarioTextField.getText()));
        p.setCantidad(Integer.parseInt(cantidadTextField.getText()));
        
        NewLogin.productos.add(p); 
        
        JOptionPane.showMessageDialog(this, "Se a registrado el siguiente producto"+" "+nombreTextField);
        
       nombreTextField.setText("");
       descProdTextField.setText("");
       pUnitarioTextField.setText("");
       cantidadTextField.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int i = -1;
        
        for(int j = 0; j<NewLogin.productos.size(); j++){
            if(NewLogin.productos.get(j).getNombreProd().equals(nombreTextField.getText())){
                i = j;
                break;
            }
        }
        if(i>=0){
            NewLogin.productos.remove(i);
            JOptionPane.showMessageDialog(this, "Se elimino el registro con exito");
        }
        else{
            JOptionPane.showMessageDialog(this, "El producto no existe, favor verifique!!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

  
